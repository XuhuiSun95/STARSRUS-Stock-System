package Classes;


public class Customer{
    private String name;
    private String phone_num;
    private String email;
    private String taxID;
    private String username;


	public Customer(String name, String phone_num, String email, String taxID, String username) {
		this.name = name;
		this.phone_num = phone_num;
		this.email = email;
		this.taxID = taxID;
		this.username = username;
	}
}
